package javastudy;

public class DataType {

    public static void main(String[] args) {
        int a = 10; // 정수
        long l = 10l;
        float f = 10.0f; // 실수
        double d = 10.0; // 실수
        String string = ""; // 문자열(여러글자)
        char chr = 'a'; // 문자(한글자)
        boolean bool = true; // 참 거짓
    }
}
