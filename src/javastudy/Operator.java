package javastudy;

public class Operator {

    public static void main(String[] args) {
        int a = 10;
        double b = 20.0;

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);
    }
}
